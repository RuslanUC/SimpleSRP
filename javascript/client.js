const getRanHex = (size_bytes) => {
    let result = "";
    let hexRef = "0123456789abcdef";

    for (let n = 0; n < size_bytes*2; n++) {
        result += hexRef[(Math.random() * 16) >> 0];
    }
    return result;
}

function concatUint8Arrays(arrays) {
    // sum of individual array lengths
    let totalLength = arrays.reduce((acc, value) => acc + value.length, 0);
    let result = new Uint8Array(totalLength);
    let length = 0;
    for(let array of arrays) {
        result.set(array, length);
        length += array.length;
    }

    return result;
}

class SrpBigInteger {
    constructor(value) {
        if(value instanceof Uint8Array)
            value = value.reduce((str, byte) => str + byte.toString(16).padStart(2, '0'), '');
        this._length = value.length;
        this._bigInt = BigInt("0x"+value);
    }

    _copy() {
        let bi = new SrpBigInteger("00");
        bi._bigInt = this._bigInt;
        return bi;
    }

    _getBigInt(val) {
        if(val instanceof SrpBigInteger)
            return val._bigInt;
        if(typeof(val) !== "bigint")
            return BigInt(val);
        return val;
    }

    // https://stackoverflow.com/questions/5989429/pow-and-mod-function-optimization
    _expmod(base, exponent, modulus){
        if (exponent === 0n) return 1n;
        if (exponent % 2n === 0n) {
            return (this._expmod(base, (exponent / 2n), modulus) ** 2n) % modulus;
        } else {
            return (base * this._expmod(base, (exponent - 1n), modulus)) % modulus;
        }
    }

    modPow(exponent, modulus) {
        exponent = this._getBigInt(exponent);
        modulus = this._getBigInt(modulus);
        let newBigInt = this._copy();
        newBigInt._bigInt = this._expmod(newBigInt._bigInt, exponent, modulus);
        return newBigInt;
    }

    toString(radix) {
        return this._bigInt.toString(radix);
    }

    toStringHex() {
        let hex = this._bigInt.toString(16);
        if(hex.length % 2 !== 0) hex = "0"+hex;
        hex = hex.padStart(this._length, "0");
        return hex;
    }

    bitLength() {
        return this._bigInt.toString(2).length;
    }

    mod(modulus) {
        modulus = this._getBigInt(modulus);
        let newBigInt = this._copy();
        newBigInt._bigInt = newBigInt._bigInt % modulus;
        return newBigInt;
    }

    multiply(multiplier) {
        multiplier = this._getBigInt(multiplier);
        let newBigInt = this._copy();
        newBigInt._bigInt = newBigInt._bigInt * multiplier;
        return newBigInt;
    }

    subtract(int) {
        int = this._getBigInt(int);
        let newBigInt = this._copy();
        newBigInt._bigInt = newBigInt._bigInt - int;
        return newBigInt;
    }

    add(int) {
        int = this._getBigInt(int);
        let newBigInt = this._copy();
        newBigInt._bigInt = newBigInt._bigInt + int;
        return newBigInt;
    }

    compareTo(other) {
        other = this._getBigInt(other);
        let newBigInt = this._copy();
        newBigInt._bigInt = other - newBigInt._bigInt;
        return newBigInt;
    }

    toUint8Array() {
        return Uint8Array.from(this.toStringHex().match(/.{1,2}/g).map((byte) => parseInt(byte, 16)));
    }
}

class SrpClient {
    constructor(username, password, hash_alg, ng) {
        this.username = username;
        this.password = password;
        this.hash_alg = hash_alg;

        this.N = new SrpBigInteger(NG_VALUES[ng]["N"]);
        this.g = new SrpBigInteger(NG_VALUES[ng]["g"]);
    }

    async hashBytes(...bytearrs) {
        let bytearr = concatUint8Arrays(bytearrs);
        const hashBuffer = await crypto.subtle.digest(this.hash_alg.toUpperCase(), bytearr);
        return new SrpBigInteger(new Uint8Array(hashBuffer));
    }

    async hashString(str) {
        return await this.hashBytes(new TextEncoder().encode(str));
    }

    async preCalculateK() {
        this.k = await this.hashBytes(this.N.toUint8Array(), this.g.toUint8Array());
        return this.k;
    }

    genSalt() {
        this.salt = new SrpBigInteger(getRanHex(32));
        return this.salt;
    }

    setSalt(salt) {
        this.salt = salt;
    }

    async genX() {
        this.x = await this.hashBytes(this.salt.toUint8Array(), (await this.hashString(`${this.username}:${this.password}`)).toUint8Array());
        return this.x;
    }

    async genV() {
        this.V = this.g.modPow(await this.genX(), this.N);
        return this.V;
    }

    async genA() {
        this.a = (new SrpBigInteger(getRanHex(128))).mod(this.N);
        if(this.a.mod(this.N)._bigInt === 0n)
            this.a = this.N.subtract(1);
        this.A = this.g.modPow(this.a, this.N);
        return this.A;
    }

    async calcM() {
        let Hg = await this.hashBytes(this.g.toUint8Array());
        let HN = await this.hashBytes(this.N.toUint8Array());
        let HI = await this.hashString(this.username);
        Hg._length = 64;
        HN._length = 64;
        let HgU = Hg.toUint8Array();
        let HNU = HN.toUint8Array();
        let HxorU = new Uint8Array(HgU.length);
        for(let i = 0; i < HgU.length; i++) {
            HxorU[i] = HgU[i] ^ HNU[i];
        }
        let Hxor = new SrpBigInteger(HxorU);
        this.M = await this.hashBytes(
            Hxor.toUint8Array(),
            HI.toUint8Array(),
            this.salt.toUint8Array(),
            this.A.toUint8Array(),
            this.B.toUint8Array(),
            this.K.toUint8Array()
        );
        return this.M;
    }

    setB(B) {
        this.B = B;
    }

    async processChallenge() {
        let u = await this.hashBytes(this.A.toUint8Array(), this.B.toUint8Array());
        let x = await this.genX();
        this.S = this.B.subtract(this.k.multiply(this.g.modPow(x, this.N))).modPow(this.a.add(u.multiply(x)), this.N);
        this.K = await this.hashBytes(this.S.toUint8Array());
        await this.calcM();
        this.H_AMK = await this.hashBytes(this.A.toUint8Array(), this.M.toUint8Array(), this.K.toUint8Array());
        return this.M;
    }

    verify_HAMK(server_HAMK) {
        return server_HAMK.compareTo(this.H_AMK) === 0;
    }
}